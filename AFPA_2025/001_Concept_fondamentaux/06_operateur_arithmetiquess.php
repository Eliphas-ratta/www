<?php

    // Déclaration de deux variables avec des valeurs numériques
    $a = 10; // $a est initialisé à 10
    $b = 5;  // $b est initialisé à 5

    // Affichage de la somme des deux variables
    echo $a + $b; 
    // Effectue l'addition : 10 + 5 = 15
    // Affiche : 15

    // Affichage de la différence des deux variables
    echo $a - $b; 
    // Effectue la soustraction : 10 - 5 = 5
    // Affiche : 5

    // Affichage du produit des deux variables
    echo $a * $b; 
    // Effectue la multiplication : 10 * 5 = 50
    // Affiche : 50

    // Affichage du quotient des deux variables
    echo $a / $b; 
    // Effectue la division : 10 / 5 = 2
    // Affiche : 2

    // Affichage du reste de la division (modulo) des deux variables
    echo $a % $b; 
    // Effectue le modulo : 10 % 5 = 0 (10 est divisible par 5 sans reste)
    // Affiche : 0

    // Incrémentation de $a par la valeur de $b
    $a += $b; 
    // Équivaut à $a = $a + $b.
    // Après cette opération, $a devient 10 + 5 = 15.

    // Décrémentation de $a par la valeur de $b
    $a -= $b; 
    // Équivaut à $a = $a - $b.
    // Après cette opération, $a revient à 15 - 5 = 10.

    // Multiplication de $a par la valeur de $b
    $a *= $b; 
    // Équivaut à $a = $a * $b.
    // Après cette opération, $a devient 10 * 5 = 50.

?>
